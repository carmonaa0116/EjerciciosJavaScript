let n = parseInt(prompt("Escribe un número del 1 al 12 y te diré el mes\n"));

switch (n) {
    case 1:
        console.log("Tu mes es Enero");
        break;
    case 2:
        console.log("Tu mes es Febrero");
        break;
    case 3:
        console.log("Tu mes es Marzo");
        break;
    case 4:
        console.log("Tu mes es Abril");
        break;
    case 5:
        console.log("Tu mes es Mayo");
        break;
    case 6:
        console.log("Tu mes es Junio");
        break;
    case 7:
        console.log("Tu mes es Julio");
        break;
    case 8:
        console.log("Tu mes es Agosto");
        break;
    case 9:
        console.log("Tu mes es Septiembre");
        break;
    case 10:
        console.log("Tu mes es Octubre");
        break;
    case 11:
        console.log("Tu mes es Noviembre");
        break;
    case 12:
        console.log("Tu mes es Diciembre");
        break;
    default:
        console.log("Debes de escribir un número del 1 al 12");
}